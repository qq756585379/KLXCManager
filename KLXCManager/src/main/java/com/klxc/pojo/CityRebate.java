package com.klxc.pojo;

public class CityRebate {
    private int tid;
    private String city_name;
    private float city_rebate;

    public int getTid() {
        return tid;
    }

    public void setTid(int tid) {
        this.tid = tid;
    }

    public String getCity_name() {
        return city_name;
    }

    public void setCity_name(String city_name) {
        this.city_name = city_name;
    }

    public float getCity_rebate() {
        return city_rebate;
    }

    public void setCity_rebate(float city_rebate) {
        this.city_rebate = city_rebate;
    }

}
