package com.klxc.pojo;

public class PayForInfo {
	private int id;
	private String bank_name;
	private String card_no;
	private String sfz_up_url;
	private String sfz_down_url;
	private String state;
	private String exam_url;
	private String create_time;

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getBank_name() {
		return bank_name;
	}

	public void setBank_name(String bank_name) {
		this.bank_name = bank_name;
	}

	public String getCard_no() {
		return card_no;
	}

	public void setCard_no(String card_no) {
		this.card_no = card_no;
	}

	public String getSfz_up_url() {
		return sfz_up_url;
	}

	public void setSfz_up_url(String sfz_up_url) {
		this.sfz_up_url = sfz_up_url;
	}

	public String getSfz_down_url() {
		return sfz_down_url;
	}

	public void setSfz_down_url(String sfz_down_url) {
		this.sfz_down_url = sfz_down_url;
	}

	public String getState() {
		return state;
	}

	public void setState(String state) {
		this.state = state;
	}

	public String getExam_url() {
		return exam_url;
	}

	public void setExam_url(String exam_url) {
		this.exam_url = exam_url;
	}

	public String getCreate_time() {
		return create_time;
	}

	public void setCreate_time(String create_time) {
		this.create_time = create_time;
	}

}
